#string operations in python

#create a string using quotes assign it to a varible

first_name = 'john'
last_name = 'smith'

pi = 3.14

# use + operator to concatenate string
full_name = first_name +' ' + last_name
print('full_name is',full_name)

#use str() constructor function to convert an integer to string
print('the value of pi is',str(pi))

#using [] brackets to get substring of string
print(full_name[5:])

